#Typecasting refers to changing the datatype of some data
a ="3"
print(type(a))
print(a)
a = int(a) #Changing the datatype to integer
print(type(a))
print(a)
a = float(a) #Changing the datatype to float
print(type(a))
print(a)
a= bool(a) #Changing the datatype to bool
print(type(a))
print(a)

